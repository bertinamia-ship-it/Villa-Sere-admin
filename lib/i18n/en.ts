// Helper function para acceder a traducciones con interpolación
export function t(key: string, params?: Record<string, any>): string {
  const keys = key.split('.')
  let value: any = enTranslations
  
  for (const k of keys) {
    value = value?.[k]
    if (value === undefined) {
      // Only log in development
      if (process.env.NODE_ENV === 'development') {
        console.warn(`[i18n EN] Translation key not found: "${key}"`)
      }
      return key
    }
  }
  
  if (typeof value !== 'string') {
    // Only log in development
    if (process.env.NODE_ENV === 'development') {
      console.warn(`[i18n EN] Translation value is not a string: "${key}" (got ${typeof value})`)
    }
    return key
  }
  
  // Interpolación simple
  if (params) {
    return value.replace(/\{(\w+)\}/g, (match, paramKey) => {
      return params[paramKey] !== undefined ? String(params[paramKey]) : match
    })
  }
  
  return value
}

